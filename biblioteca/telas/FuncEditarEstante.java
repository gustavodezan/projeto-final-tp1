    /*Pega os valores escritos nos campos de texto e se forem validos edita a estante
    selecionada ao apertar o botão*/
    
    private void btConfEdtEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfEdtEstActionPerformed
        int ind = tblEst.getSelectedRow();
        if (ind >=0 && ind < estantes.size()){
            String id = tfEdtId.getText();
            String local = tfEdtLoc.getText();
            String genero = tfEdtGen.getText();


            if (!id.equals("") && !local.equals("") && !genero.equals("")){
                estantes.get(ind).setIdentificacao(id);
                estantes.get(ind).setLocalizacao(local);
                estantes.get(ind).setGenero(genero);
                tfEdtId.setText("");
                tfEdtLoc.setText("");
                tfEdtGen.setText("");
            }else{
                JOptionPane.showMessageDialog (null,"valor(es) invalido(s)");
                tfEdtId.setText("");
                tfEdtLoc.setText("");
                tfEdtGen.setText("");
            }
            carregarTabela();
        }
    }//GEN-LAST:event_btConfEdtEstActionPerformed
    
