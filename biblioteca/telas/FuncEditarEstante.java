    /*Ao clickar para selecionar alguma estante na tabela , os dados da tabela
    aparecem nos campos de texto,ajudando a mostrar qual estante esta sendo atualizada*/
    
    private void tblEstMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEstMouseClicked
        int ind = tblEst.getSelectedRow();
        if (ind >=0 && ind < estantes.size()){
            Estante estante1 = estantes.get(ind);
            tfEdtId.setText(estante1.getIdentificacao());
            tfEdtLoc.setText(estante1.getLocalizacao());
            tfEdtGen.setText(estante1.getGenero());
        }
    }//GEN-LAST:event_tblEstMouseClicked

